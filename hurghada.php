<table style='width:100%'><tr><td><h2>14/06/2001</h2></td><td align="right"><a class="travel2" href="#top"><img style="border:0px"  src="images/top_blue.png" alt="top"/></a></td></tr></table>
<p>
Le matin, plong&eacute;e sur le site de Umm Gamar North West. Parmi d'autres poissons, des diodons et une mur&egrave;ne grise. Un moniteur d'une palanqu&eacute;e a vu un requin, il a essay&eacute; de le suivre, et sa palanqu&eacute; a essay&eacute; de le suivre (le moniteur).
