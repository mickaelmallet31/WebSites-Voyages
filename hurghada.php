</p>

<p>
Le soir (&agrave; faire absolument), plong&eacute; de nuit sur Abu Galawa Laguna. Rien &agrave; voir avec le jour (vous vous en doutiez): les rep&egrave;res sont diff&eacute;rents, bcp moins de choses &agrave; voir, des requins &agrave; surveiller (je plaisante, on n'a pas eu la chance d'en voir) et de fortes sensations. Les d&eacute;butants qui avaient eu leur dipl&ocirc;me de niveau la veille ont pu venir. Alors qu'est ce qu'on a vu ? Des perroquets endormis (ils sont comme tout le monde), un magnifique poulpe changeant de couleur d&egrave;s qu'il arr&ecirc;tait de se d&eacute;placer (quel rapidit&eacute; dans le mim&eacute;tisme alors !), des m&eacute;rous loutres, des &eacute;ponges rouges et ... et... une tortue g&eacute;ante. Une magnifique tortue qu'on a r&eacute;veill&eacute; dans son trou avec nos lampes (fournies par le club) et qui n'&eacute;tait pas contente du tout. On a pass&eacute; pratiquement une heure au fond (59 mn pour &ecirc;tre pr&eacute;cis) et c'est un des plus souvenirs que je garde de mon s&eacute;jour &agrave; Hurghada.
</p>

