<p>
Aujourd'hui, c'est safari. Pour ceux qui se sont inscrits &agrave; l'option "Sensation fortes avec les b&eacute;douins", veuillez vous assoir dans un 4x4 et ne bougez plus jusqu'&agrave; l'ouverture des portes (c'est dur de ne pas bouger vu l'&eacute;tat de la piste). Pour profiter encore un peu plus de la vue, je me place sur la galerie de la jeep, avec le cameraman qui filme notre exp&eacute;dition dans le d&eacute;sert.
</p>

<p>
Arriv&eacute; au camp des b&eacute;douins qui vivent maintenant du tourisme (pour ceux-l&agrave; en tout cas), il fait encore tr&egrave;s chaud. On n'est pas en pleine forme, on manque d'eau (Wa&euml;l aurait pu nous conseiller d'en ramener plus). Comme il fait trop chaud pour sortir (nous sommes sous une paillasse), on attend longtemps ou on fume la chicha (le narghil&eacute;). Enfin les activit&eacute;s commencent: le tour en dromadaire. Le cameraman n'arr&ecirc;te pas de courrir. Nous allons voir le puit d'eau (le soir, l'eau est plus sal&eacute; &agrave; cause de la Mer Rouge). Nous gravissons la montagne pour observer le coucher de soleil. L'organisateur nous dit que sur le film, il y aura comme musique de fond C&eacute;line Dion. Apr&egrave;s &ccedil;&agrave;, on ne peut pas refuser d'acheter la cassette (&agrave; 100£ egyptiennes). Et pour finir repas de mariage traditionnel des b&eacute;douins.
</p>

<p>
Ce safari n'a pas l'unanimit&eacute; parmi mes comp&egrave;res. De toute fa&ccedil;on, il n'y avait rien &agrave; faire &agrave; Hurghada &agrave; part se griller la pillule au soleil. Donc... Un conseil pour la prochaine fois, partez plus tard.
</p>

<p>Ca y est. L'heure du d&eacute;part a sonn&eacute;. Pr&eacute;vu &agrave; 2h55, nous partirons vers 5h.
</p>

