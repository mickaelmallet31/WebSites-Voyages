<p>
Le matin, plong&eacute;e sur le site de North Abu Ramada. Certaines palanqu&eacute;es sont partis sur ce site en plong&eacute;e d&eacute;rivante, c'est &agrave; dire qu'ils sautent du bateau pdt que le bateau avance. Parmi d'autres poissons, des poissons crocodiles qui ne d&eacute;m&eacute;ritent pas leur nom (impressionnant), des poissons pierres, des poissons scorpions, des rascasses, des alciones, des gorgones... Tr&egrave;s jolie plong&eacute;e.
