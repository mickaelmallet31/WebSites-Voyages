<h1 style="text-align: center">R&eacute;cit du s&eacute;jour au centre UCPA de Plong&eacute;e &agrave; Hurghada</h1>

<table style='width:100%'><tr><td><h2>09/06/2001</h2></td><td align="right"><a class="travel2" href="#top"><img style="border:0px"  src="images/top_blue.png" alt="top"/></a></td></tr></table>
<p>
D&eacute;part de Paris Roissy-Charles de Gaulle &agrave; 20h. Arriv&eacute;e &agrave; Hurghada &agrave; 1h du matin le dimanche. Accueilli par Emmanuelle, la responsable du Centre UCPA d'Hurghada et Wa&euml;l, le responsable des activit&eacute;s touristiques. Nous sommes log&eacute;s &agrave; l'h&ocirc;tel Geisun (Cornich Street El Dhar T&eacute;l.: 00 20 65 54 93 77 Fax: 00 20 65 54 84 80), petit h&ocirc;tel 2 &eacute;toiles.
</p>

<p>
Comme nous sommes dans le d&eacute;sert, l'h&ocirc;tel est ravitaill&eacute; en eau potable 2 fois par jour par camion. La pression des douches se fait par la force de gravit&eacute; (les r&eacute;servoirs d'eau sont sur les toits des batiments) et l'eau chauff&eacute;e &agrave; l'&eacute;nergie solaire. On ne jette pas de papier toilette dans les toilettes (il y a une poubelle au pied), &ccedil;&agrave; pourrait boucher la tuyauderie. Apr&egrave;s tous ces conseils, la r&eacute;partition des chambres et les renseignements pour former les palanqu&eacute;es de plong&eacute;e, nous allons au lit (il est quand m&ecirc;me 2h et demi). Et d&eacute;couvrons notre chambre: une clim qui n'est pas des moins sonore, une t&eacute;l&eacute;vision sans fil d'antenne (de toute fa&ccedil;on, je suis en vacances), une salle de bain correcte et 2 bons lits. Les moustiques ne tardent pas &agrave; nous d&eacute;voiler leur pr&eacute;sence. Pr&eacute;voyez donc citronnelle et autre chasse-b&eacute;b&ecirc;te qui pique.
</p>

<table style='width:100%'><tr><td><h2>10/06/2001</h2></td><td align="right"><a class="travel2" href="#top"><img style="border:0px"  src="images/top_blue.png" alt="top"/></a></td></tr></table>
<p>
D&eacute;couverte de l'h&ocirc;tel en plein jour. La plage (interdite aux non-r&eacute;sidentiels des h&ocirc;tels) &agrave; 50 m&egrave;tres, le sable pas tr&egrave;s fin et des oursins pendant 20 m&egrave;tres. Prenez vos tongues. Pour le petit d&eacute;j, grand choix dans la vari&eacute;t&eacute;, aussi bien sucr&eacute; que sal&eacute;. On y retrouve les patisseries &eacute;gyptiennes, les oeufs brouill&eacute;s, caf&eacute;, chocolat chaud, th&eacute;...
</p>

<p>
Apr&egrave;s un petit d&eacute;jeuner, nous allons au centre de plong&eacute;e qui se situe au bord de la plage. Une pr&eacute;cision au sujet du centre: UCPA sous-traite la plong&eacute;e avec le centre qui est tenu par des Allemands ma foi fort sympathiques. C'est la distribution du mat&eacute;riel de plong&eacute;e. Nous d&eacute;couvrons nos moniteurs: S&eacute;verine, la Suisse, Peter et Denis 2 Allemands, Osama et Marmoud 2 Egyptiens et Emmanuelle, notre responsable du centre. On arrive &agrave; se comprendre en parlant franglais.
</p>

<p>
L'apr&egrave;s-midi, PMT pr&egrave;s du ponton (apr&egrave;s les oursins de la plage) o&ugrave; l'on peut voir d&eacute;j&agrave; des poissons-perroguets, des poissons-anguilles...
</p>

