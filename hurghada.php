<table style='width:100%'><tr><td><h2>13/06/2001</h2></td><td align="right"><a class="travel2" href="#top"><img style="border:0px"  src="images/top_blue.png" alt="top"/></a></td></tr></table>
<p>
Le matin, plong&eacute;e sur le site de Shaab El Erg. En autres on a pu observer (eh oui, pour changer un peu) des poissons globes masqu&eacute;s (non, ce n'est pas Zorro) et des poissons coffres.
