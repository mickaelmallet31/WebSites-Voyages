<p>
L'apr&egrave;s-midi, plong&eacute;e sur le site de Shab Fanadir. On a pu y voir des poissons ballons, des b&eacute;nitiers, plein de coraux tabulaires et des dollars des sables (vous pouvez les laisser au fond, ceux-l&agrave; ne sont pas accept&eacute;s dans les banques).
