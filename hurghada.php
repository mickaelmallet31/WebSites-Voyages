<p>
L'apr&egrave;s-midi, plong&eacute;e sur le site de Shaabruhr Umm Gamar. On a pu y voir des mur&egrave;nes g&eacute;antes, une &eacute;pave et des nasons.
</p>

<p>
Le soir, pour changer du Papa's bar, Wa&euml;l nous am&egrave;ne dans un bar &eacute;gyptien, en terrasse o&ugrave; on a pu jouer avec lui au domino (il a perdu !!!). Le cocktail de fruit est tr&egrave;s demand&eacute; (&ccedil;&agrave; ressemble plus &agrave; une salade de fruits mais c'est quand m&ecirc;me d&eacute;licieux). Autrement on y retrouve les traditionnels carcadi et th&eacute; &agrave; la menthe. De retour au centre, &agrave;pr&egrave;s le diner, Emmanuelle nous fait un diaporama sur les bestioles qu'on peut voir sous l'eau.
