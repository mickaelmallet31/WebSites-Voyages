<h1 style="text-align: center">R&eacute;cit de la croisi&egrave;re aux Iles Vierges Britanniques<br/>
du 5/12/97 au 12/12/97</h1>

<h2>1. Le vendredi soir &agrave; 19h (5/12/97)</h2>
<p>
Nous arrivions &agrave; l'a&eacute;roport de St-Martin dans la partie hollandaise, un taxi de la soci&eacute;t&eacute; de location Stardust est venu nous chercher. On a alors retrouve Fred et ED arrives plus t&ocirc;t dans la journ&eacute;e pour pr&eacute;parer le bateau et faire quelques courses. Tout est OK on pars direction plein est vers les &icirc;les vierges (22h) on n'a pas beaucoup de vent est la travers&eacute;e se fera au moteur nous sommes 8 plus le skipper (Fred) on va organiser des tours de quart de 2h. La mer est tr&egrave;s calme, les &eacute;toiles brillent dans le ciel noir c'est magnifique une &eacute;quipe aura la chance de voir passer 2 paquebots ressemblants &agrave; des sapins de No&euml;l recouverts de guirlandes.
</p>

<h2>2. Samedi 6/12/97</h2>
<p>
Au petit matin un &icirc;le &eacute;tait en vue, nous avions fait bon cap !!! Notre premi&egrave;re &icirc;le : Virgin Gorda. St Thomas bay o&ugrave; nous d&eacute;jeunons avant d'aller &agrave; terre pour effectuer les formalit&eacute;s de douane. Fatale erreur car le bureau ferme &agrave; midi (nous reviendrons donc le lendemain mais le dimanche, les autorisations co&ucirc;tent plus cher. Enfin pour pr&egrave;s de 200 US$, nous obtenons le droit de navigation dans les eaux britanniques (British Virgin Islands) ; nous ne pourrons pas aller sur les &icirc;les am&eacute;ricaines qui n&eacute;cessitent en plus un visa).
</p>
<p>
Nous partons pour une autre &icirc;le (&agrave; 5 miles &agrave; peine), Marina Cay pour nous mettre au mouillage pour la nuit. Ravis de nous coucher apr&egrave;s la nuit en pleine mer<br/>
</p>

<h2>3. Dimanche 7/12/97</h2>
<p>
Le lendemain au petit jour, nous levons l'encre pour rejoindre le sud de Virgin Gorda et un endroit appelle "les BATHs". Ce sont de gros rochers en bord de mer sur le sable blanc et les cocotiers. Super !!! En plus, l'eau est tr&egrave;s tr&egrave;s claire et les fonds sous-marins magnifiques ! &Ccedil;a commence bien. L'apr&egrave;s-midi, nous partons faire un tour au nord de Virgin Gorda et nous trouvons un mouillage pr&egrave;s d'une petite plage d&eacute;serte sur "Prickly Pear Island".
</p>
<p>
Nous d&eacute;barquons pour y monter au sommet (rencontre avec des cactus et quelques ch&egrave;vres) ; le coucher de soleil est lui aussi tr&egrave;s beau.
</p>

<h2>4. Lundi 8/12/97</h2>
<p>
Nous reprenons le cap &agrave; l'est en passant au nord des "Dog Island" et "Great Camanoe" pour arriver sur "Guana Island". Encore un &icirc;le priv&eacute;e mais nous approchons d'une superbe plage ; nous d&eacute;jeunerons dans "White Bay" ; apr&egrave;s, un peu de plong&eacute;e o&ugrave; les poissons sont aussi tr&egrave;s nombreux. D'ailleurs les p&eacute;licans le savent et nous les observons en train de p&eacute;cher en faisant de plongeons impressionnants.
</p>

<p>
Dans l'apr&egrave;s midi, nous longeons la c&ocirc;te Nord de Tortola (la plus grande &icirc;le : 10 miles de long) pour aller dormir &agrave; "Cane Garden Bay" ; nous descendrons &agrave; terre pour boire quelques verres de cocktails locaux puis nous finissons la soir&eacute;e avec nos propres r&eacute;serves (Rhum Neison, trois rivi&egrave;res, punch banane, bi&egrave;res...).
</p>

<h2>5. Mardi 9/12/97</h2>
<p>
D&eacute;part pour "Sandy Cay", une toute petite &icirc;les que nous atteignons vers 8h ; on nage pour arriver sur une &icirc;le de r&ecirc;ve, on s'imagine Robinson Crusoe ; nous marchons sur le sable de cette &icirc;le d&eacute;serte et dans la v&eacute;g&eacute;tation, c'est GENIAL !!! L'eau est bleue bleue comme on ne la voie que sur les cartes postales et pourtant on y est ! Nous montons en haut du mat d'ou nous dominons toute &icirc;le.
</p>
<p>
Peu avant midi, nous allons juste &agrave; c&ocirc;t&eacute; (1 mile) sur une autre &icirc;le encore plus petite "Sandy Spitt" on en fait vite le tour a pied, un amas de sable avec 3,4 palmiers au milieu on fait des photos type carte postale ! Puis nous allons encore sur une autre &icirc;le "Jost Van Dyke" pour tenter de faire quelques courses (on ne trouve qu'un petite &eacute;picerie) et prendre un ap&eacute;ro au "foxy's". Le mal de terre nous remm&egrave;nera assez vite sur le bateau pour le d&icirc;ner.
</p>

<h2>6. Mercredi 10/12/97</h2>
<p>
Nous repassons au sud de Tortola, un arr&ecirc;t &agrave; "Soper's hole" ou nous trouvons une sup&eacute;rette pour nous r&eacute;approvisionner (on consomme pas mal a 9 sur un bateau) apr&egrave;s un peu de navigation nous nous arr&ecirc;tons aux "indians" pr&egrave;s de "Pelican Island" pour faire de la plong&eacute;e en apn&eacute;e.... Les plus beaux fonds sous marins jamais vus on reste des heures dans l'eau &agrave; regarder les paysages et les poissons multicolores. On finit par avoir froid m&ecirc;me dans une eau &agrave; 29°C. Nous allons nous ancrer sur "Norman Island".
</p>

<h2>7. Jeudi 11/12/97</h2>
<p>
En avant pour voir une &eacute;pave sur "Salt Island" sur 8 &agrave; 12 m de fond de bonnes apn&eacute;es en perspective, mais ca en vaut la peine. Les plongeurs en bouteille font fuir les poissons avec toutes leurs bulles, le monde du silence doit rester calme... Un poisson-ange se cache sous la coque, en passant en dessous on d&eacute;couvre un &eacute;norme h&eacute;lice (2 m de diam&egrave;tre) et plein de poissons &eacute;trange aussi surpris que moi de la rencontre.
</p>

<p>
L'apr&egrave;s-midi se termine et nous songeons &agrave; notre derni&egrave;re nuit sur le bateau. Nous la passerons au BATHs vraiment un super endroit, couchers de soleil j'ai h&acirc;te de voir le photos.
</p>

<h2>8. Vendredi 12/12/97</h2>
<p>
De retour sur Tortola pour rendre le bateau, nous faisons quelques achats de T-shirts avant d'aller attendre l'avion a l'a&eacute;roport. Finalement apr&egrave;s 1h30 de retard nous partons des &icirc;les vierges pour atterrir &agrave; St Martin &agrave; la tomb&eacute;e de la nuit. Une nuit que nous ne voulons pas passer &agrave; l'h&ocirc;tel ; nous louons un monospace (7pl.) pour nous d&eacute;placer sur cette &icirc;le.
</p>

<p>
On a faim et une pizza sera la bienvenue (attention parfois un norv&eacute;gienne vient pour discuter et chercher de la compagnie...). Une glace italienne pour dessert avant de passer la soir&eacute;e dans les bar-disco !!! On finira couch&eacute;s dans le sable sur une plage. Le lendemain, on fait le tour de l'&icirc;le (10km de large) puis on tra&icirc;ne dans la rue des commerces : bijouterie, hi-fi, montre toutes les grandes marques &agrave; petits prix. Personne ne se laisse tenter &agrave; acheter d'objets de valeur (Antoine a failli craquer pour une montre Breitling a 5400 Fr. au lieu de 8000 ...).
</p>

<p>
Apr&egrave;s midi, un tour FATAL sur une plage nous vaudra le vol du mat&eacute;riel photo de Fred avec ses papiers, idem pour le porte feuille de Sylvain !!! Avec un avion pr&eacute;vu pour 15h30 ca a &eacute;t&eacute; la course au changement de vol et annulation de carte bleue. Enfin Fred d&eacute;cale son vol afin d'obtenir un papier de la gendarmerie confirmant son identit&eacute; nous repartons vers 16h en laissant Fred qui arrivera a rentrer sur Fort de France dans la soir&eacute;e.<br/>
Voil&agrave; en gros une formidable croisi&egrave;re sur les plus belles &icirc;les des Cara&iuml;bes.<br/>
</p>

